public class Example10 {

	public static void main(String[] args) {
		char ch='A';
		switch(ch)
		{
		  case 'A':
		          System.out.println("Given character is vowel "+ch);
		          break;
		  case 'E':
	          System.out.println("Given character is vowel "+ch);
	          break;
		  case 'I':
	          System.out.println("Given character is vowel "+ch);
	          break;
		  case 'O':
	          System.out.println("Given character is vowel "+ch);
	          break;
		  case 'U':
	          System.out.println("Given character is vowel "+ch);
	          break;
	          default:
	        	  System.out.println("Given character is not a vowel");
              break;
		}

	}

}
